    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
         JFileChooser chooser = new JFileChooser();

    // ✅ Carpeta permitida
    String rutaUsuario = System.getProperty("user.home");
    File carpetaPermitida = new File(rutaUsuario + "/Desktop/CortesDeCaja/");
    chooser.setCurrentDirectory(carpetaPermitida);

    chooser.setMultiSelectionEnabled(true);
    chooser.setDialogTitle("Selecciona archivos desde CortesDeCaja");

    int opcion = chooser.showOpenDialog(this);
    if (opcion == JFileChooser.APPROVE_OPTION) {
        File[] archivosSeleccionados = chooser.getSelectedFiles();
        boolean todosValidos = true;

        for (File archivo : archivosSeleccionados) {
            if (archivo.getAbsolutePath().startsWith(carpetaPermitida.getAbsolutePath())) {
                modeloArchivos.addElement(archivo); // ✅ Aceptado
            } else {
                todosValidos = false;
            }
        }

        if (modeloArchivos.isEmpty()) {
            lblAdvertencia.setText("Error al cargar archivos, intenta de nuevo");
            lblAdvertencia.setForeground(Color.red);
        } else if (!todosValidos) {
            lblAdvertencia.setText("Solo se permiten archivos de 'CortesDeCaja'");
            lblAdvertencia.setForeground(Color.orange);
        } else {
            lblAdvertencia.setText("Tus archivos se cargaron con éxito");
            lblAdvertencia.setForeground(Color.WHITE);
        }
    }
    }//GEN-LAST:event_btnAñadirActionPerformed

