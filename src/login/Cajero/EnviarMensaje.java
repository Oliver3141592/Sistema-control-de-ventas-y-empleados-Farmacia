    private void btnMandarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMandarActionPerformed
        // TODO add your handling code here:
        miCorreo.setRemitente(remitente,contraseña);
        miCorreo.setDestinatario(txtcorreo.getText());
        miCorreo.setContenido(txtAsunto.getText(),txtMensaje.getText());
        for (int i = 0; i < modeloArchivos.size(); i++) {
            miCorreo.agregarArchivo(modeloArchivos.get(i).getAbsolutePath());
        }
        try {
        // Envía el correo
        miCorreo.enviarCorreo();
        lblAdvertencia.setText("Correo envíado correctamente:)");
        lblAdvertencia.setForeground(Color.WHITE);

        } catch (Exception e) {
            lblAdvertencia.setText("Correo no envíado, revisa que el correo sea válido");
            lblAdvertencia.setForeground(Color.red);
            
        }
    }//GEN-LAST:event_btnMandarActionPerformed

